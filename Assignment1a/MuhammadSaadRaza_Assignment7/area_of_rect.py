
def calculate_area(length, width):
    return length * width

# Calling the function with different values
area1 = calculate_area(12, 7)
area2 = calculate_area(7.7, 1.2)
print(f"Area of rectangle with length 5 and width 10: {area1}")
print(f"Area of rectangle with length 7.5 and width 3.2: {area2}")
